        <h4>Song profiles</h4>
        <p>
            The process of scoring the difficulty of a file also produces various informational output designed to give players as accurate as possible the essence of a file at a glance, such as nps graphs difficulty adjusted graphs and chord density graphs. Additional file data will be available as well including length number of chords and mines etc. Song profiles will also include a preview of the chart.
        </p>

        <h4>Free use difficulty calculator</h4>
        <p>
            The algorithm the calculator employs assumes a file is played 4-key spread style on the keyboard. While the index of ranked files is not intended to be "exclusive" or "elitist" there will be some measure of quality control by virtue of the process by which files are blocked.
        </p>
        <p>
            This feature is available so that you can break down any file you wish and see how it would be scored if it were a ranked file, and view any information provided by the parser. Ranked files will by convention be limited to rates incremented by 0.1 from 1 to 2, however if for some reason you play a song on 3.84 rate you can manually alter the bpm of the file you wish to look at to reflect this rate multiplier and score the difficulty in this manner.
        </p>
        <p>
            If you manage to “break” the calculator, as in feed it a file designed to fool it that results in wildly overrated or underrated difficulty values we ask that you provide us with the file so that we can improve the system.
        </p>

        <h4>Pack index</h4>
        <p>
            This is pretty self explanatory. It’s a list of packs and download links for them. Median difficulty for the files is provided to give a cursory indication of general pack difficulty however you should defer to difficulty ratings for individual files for any level of information beyond this.
        </p>

        <h4>Player profiles</h4>
        <p>
            You can view submitted scores for yourself on your profile and submitted scores of others on their respective profiles. Profiles will also indicate a player’s rating for each skillset and overall skill.
        </p>

        <h4>Resource list</h4>
        <p>
            This is a central location for various resources pertaining to stepmania in an informational or gameplay capacity. Different versions of the game as well as themes and noteskins will be available for download with brief descriptions of their design or intention. A clearly defined list of terminology is available as well as other informational resources.
        </p>

        <h4>Individual file rankings</h4>
        <p>
            While the heart of the algorithm is designed to provide a numerical value for difficulty to achieve “AA” grade the process by which this is conducted is not strictly limited to this usage. It is possible to repurpose the algorithm to reverse look up a specific score (dp%) on a file to rate the difficulty of achieving that score on the file in question. I expect the results to be fairly accurate between 91-95%.
        </p>
