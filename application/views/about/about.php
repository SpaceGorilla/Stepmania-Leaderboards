        <h4>Overall rankings</h4>
        <p>
            Overall rankings aim to give players an aggregate score by which they can be compared to other players based on a weight of their scores in individual skillsets. Overall rankings should be taken with a grain of salt and it should be stressed that skillset rankings will be far more accurate (and narrow) indicators of relative skill levels.
        </p>

        <h4>Skillset rankings</h4>
        <p>
            Skillset specialization is a relatively simple concept. Players who enjoy certain types of files will play those files much more often than others, and as a result they will improve at those types of files and enjoy them even more. Players then tend to develop strengths in some areas and weaknesses in others that become increasingly exaggerated as they improve in overall skill. While the difficulty algorithm attempts to correlate difficulty across skills the best accuracy will always be gleaned when observing players in the same skillset, or comparing them on greatly similar files. This is where you can do that.
        </p>

        <h4>Ranked file index</h4>
        <p>
            The ranked file index is the core output of the difficulty calculator, and the basis by which players are ranked. Various skill-testing and rankings systems across other rhythm games fall short in many ways, including failing to provide a platform for players to showcase their skills in a manner that evades specific file biases.
        </p>
        <p>
            The ranked file index aims to provide players with a vast array of files which they can choose to be rated on so players can not only play to their specific strengths, but play files they enjoy in the process. The process should be play what you want and see where your scores stand, rather than playing a select few files chosen by someone else. The ranked file index will consist of commonly played files and rates from major pack releases, however in the interest of keeping with the idea of letting players choose what they wish to be judged on you can suggest that specific files and rates be added to the index as long as they meet some basic criteria.
        </p>

        <h4>A file will not be ranked for any of the following reasons:</h4>

        <p>
            - Has not been and cannot be “AA”d. <br />
            - Is assigned a wildly incorrect difficulty due to some oversight in the scoring algorithm. These instances will be investigated and the underlying cause will be rooted out and corrected, and once done files removed or blocked for this reason will be ranked assuming they do not meet any other criteria for exclusion. <br />
            - Is a “vibro” file. In practice vibro files must be considered too differently from traditional files in terms of difficulty scoring as a pad file would be. Also nobody cares about vibro.
        </p>

        <p>
            The ranked file index also provides players with a difficulty roadmap that they can use to guide their progression as they play. By consequence of design it also provides players with a “recommended” list of charts to try that are within a certain difficulty range or of a specific file type, or any intersection of the two.
        </p>

        <h4>Score submission</h4>

        <p>
            Score submission is the responsibility of the user if they wish to be ranked as players or have specific scores rated. Use of this site is a privilege and there is an expectation of honesty to score submission, both to the other users as well as yourself, however, score submission for scores that break {some percentile} are required to be approved by mods before being listed. Below this threshold you are freely able to submit your scores and be ranked regardless of whether you are totally new or an eminent figure in a community.
        </p>
        <p>
            You are forewarned that cheating or faking scores is generally taken as gravely insulting to your peers and such conduct is quickly sniffed out and met with vicious response. If you are an uninitiated player who has wandered their way into the upper echelons of skill you will be requested to provide some form of proof of play, be it a video or a livestream or an in -person meetup if you live near someone willing to host you.
        </p>
        <p>
            You will be expected to reproduce a level of play within a reasonable degree of your best scores. This is not an unreasonable request and it is a process by which all players at top levels of play have gone through repeatedly. Currently score submission is limited to files that are ranked and scores that are “AA” grade or above.
        </p>
